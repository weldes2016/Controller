package br.com.controller.galaxpay.domain;

public class GalaxPayBank {
	public String idGalaxyPay;
	public String hashGalaxPay;
	public String url;
	public String token;
	public String galaxPayId;
	
	public String getGalaxPayId() {
		return galaxPayId;
	}
	public void setGalaxPayId(String galaxPayId) {
		this.galaxPayId = galaxPayId;
	}
	public String getIdGalaxyPay() {
		return idGalaxyPay;
	}
	public void setIdGalaxyPay(String idGalaxyPay) {
		this.idGalaxyPay = idGalaxyPay;
	}
	public String getHashGalaxPay() {
		return hashGalaxPay;
	}
	public void setHashGalaxPay(String hashGalaxPay) {
		this.hashGalaxPay = hashGalaxPay;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	
	
}
